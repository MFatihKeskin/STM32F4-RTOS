/**
  * @brief This function handles EXTI line0 interrupt.
  */
void EXTI0_IRQHandler(void)
{
  /* USER CODE BEGIN EXTI0_IRQn 0 */

	BaseType_t task_woken;
	uint32_t received_number;
	// allocation stack size for static keyword
	static const char *string_names[] ={"String 0\n", "String 1\n", "String 2\n", "String 3\n"};

	task_woken = pdFALSE;

  /* USER CODE END EXTI0_IRQn 0 */
  HAL_GPIO_EXTI_IRQHandler(GPIO_PIN_0);
/* USER CODE BEGIN EXTI0_IRQn 1 */
  	// until the emptyh queue, work in while
  while(xQueueReceiveFromISR(integer_queueHandle, &received_number, &task_woken) != errQUEUE_EMPTY)
  {
	  received_number &= 0x03;
	  xQueueSendToBackFromISR(string_queueHandle,&string_names[received_number],&task_woken);
	  HAL_GPIO_TogglePin(red_led_GPIO_Port, red_led_Pin);		// 5 value was taken in queue

  }

  portYIELD_FROM_ISR(task_woken);


  /* USER CODE END EXTI0_IRQn 1 */
}

